CREATE TABLE USER (
    UserId    VARCHAR(100)    ,
    PWD    VARCHAR(100),
    NAME    VARCHAR(100)    ,
    SKILL    VARCHAR(100),
    EMAIL    VARCHAR(100)    ,
    PHONE    VARCHAR(100)    ,
    HIREDATE    DATE
);

CREATE TABLE USER_Description (
	name	VARCHAR(100),
    UserId	VARCHAR(100),
	Career	VARCHAR(100),
	SKILL1	VARCHAR(100),
	SKILL2	VARCHAR(100),
	SKILL3	VARCHAR(100),
    Introduce  VARCHAR(100)
);